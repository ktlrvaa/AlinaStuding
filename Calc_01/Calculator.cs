using System;
using System.Collections.Generic;
using System.Text;

namespace Calc_01
{
    public class Calculator
    {
        public float Summ(float a, float b)
        {
            return a + b;

        }
        public float Difference(float a, float b)
        {
            return a - b;

        }
        public float Complexity(float a, float b)
        {
            return a * b;

        }
    }
}
